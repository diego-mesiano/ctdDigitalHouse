let frutasVermelhas = ['Morango','Melancia'];
let frutasAmarelas = ['Melão','Pessego'];


// ... spread operator - operador de propagação, de espalhamento
let frutas = [...frutasVermelhas,...frutasAmarelas]; //cria um grande array
console.log(frutas);

let frutas1 = [frutasVermelhas, frutasAmarelas]; //cria uma array com arrays dentro
console.log(frutas1);


